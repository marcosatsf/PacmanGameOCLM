	ADD LIMPACX, 26
	MOV DX, LINHA
	MOV LIMPACY, DX
	ADD LIMPACY, 26
	;MOV COR, GHOSTCOLOR
	
GHOST_PIX_PRINT_CHECK:
	MOV CX, COLUNA
	MOV DX, LINHA
	MOV BX, BUFFERMAT
	CMP FANTASMA[BX], 1
	JE GHOST_PIX_PRINT
	CMP FANTASMA[BX], 2
	JE GHOST_PIX_PRINTWHITE
	MOV AL, 0
	MOV AH, 0CH ; put pixel
	INT 10H
	JMP CHECK_EOL_GHOST
	
GHOST_PIX_PRINTWHITE:
	MOV AL, 0FH
	MOV AH, 0CH
	INT 10H
	JMP CHECK_EOL_GHOST
	
GHOST_PIX_PRINT:
	MOV AL, COR
	MOV AH, 0CH ; put pixel
	INT 10H

CHECK_EOL_GHOST:
	CMP CX, LIMPACX
	JE 	GHOST_NXL
	INC COLUNA
	INC BUFFERMAT
	JMP GHOST_PIX_PRINT_CHECK

GHOST_NXL:
	CMP DX, LIMPACY
	JE	GHOST_FIM
	SUB COLUNA, 27
	INC LINHA
	INC BUFFERMAT
	JMP GHOST_PIX_PRINT_CHECK
GHOST_FIM:
	MOV BUFFERMAT,0
	RET
PRINT_GHOST ENDP  ;end ghost procedure --------------------------------

RESET_PIX PROC ;reset pixel to upper-left position -----------------------------
	MOV COLUNA, 37
	MOV LINHA, 17
	MOV AX, 27
	MUL QUADLIN
	ADD LINHA, AX
	MOV AX, 27	
	MUL QUADCOL
	ADD COLUNA, AX
	RET
RESET_PIX ENDP ;--------------------------------------------------------

CHOOSE_COLOR PROC
	CMP [MAPA + BX], 0AH
	JE REDGHOST_HERE
	CMP [MAPA + BX], 0BH
	JE ORANGEGHOST_HERE
	CMP [MAPA + BX], 0CH
	JE PINKGHOST_HERE
	CMP [MAPA + BX], 0DH
	JE CYANGHOST_HERE
REDGHOST_HERE:
	MOV COR,28H
	MOV WHICHGHOST,1
	RET
ORANGEGHOST_HERE:
	MOV COR,2BH
	MOV WHICHGHOST,2
	RET
PINKGHOST_HERE:
	MOV COR,3CH
	MOV WHICHGHOST,3
	RET
CYANGHOST_HERE:
	MOV COR,0BH
	MOV WHICHGHOST,4
	RET
CHOOSE_COLOR ENDP

